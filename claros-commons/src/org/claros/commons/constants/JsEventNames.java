package org.claros.commons.constants;

public class JsEventNames {
	
	/**
	 * Fires when the user aborts the download of an image.
	 */
	public static final String onabort = "onabort";

	/**
	 * Fires when the object is set as the active element.
	 */
	public static final String onactivate = "onactivate";
	
	/**
	 * Fires on the object immediately after its associated document prints or previews for printing.
	 */
	public static final String onafterprint = "onafterprint";
	
	/**
	 * Fires on a databound object after successfully updating the associated data in the data source object.
	 */
	public static final String onafterupdate = "onafterupdate";
	
	/**
	 * Fires immediately before the object is set as the active element.
	 */
	public static final String onbeforeactivate = "onbeforeactivate";

	/**
	 * Fires on the source object before the selection is copied to the system clipboard.
	 */
	public static final String onbeforecopy = "onbeforecopy";

	/**
	 * Fires on the source object before the selection is deleted from the document.
	 */
	public static final String onbeforecut = "onbeforecut";

	/**
	 * Fires immediately before the activeElement is changed from the current object to 
	 * another object in the parent document.
	 */
	public static final String onbeforedeactivate = "onbeforedeactivate";

	/**
	 * Fires before an object contained in an editable element enters a UI-activated 
	 * state or when an editable container object is control selected.
	 */
	public static final String onbeforeeditfocus = "onbeforeeditfocus";

	/**
	 * Fires on the target object before the selection is pasted from the system 
	 * clipboard to the document.
	 */
	public static final String onbeforepaste = "onbeforepaste";

	/**
	 * Fires on the object before its associated document prints or previews for printing.
	 */
	public static final String onbeforeprint = "onbeforeprint";

	/**
	 * Fires prior to a page being unloaded.
	 */
	public static final String onbeforeunload = "onbeforeunload";

	/**
	 * Fires on a databound object before updating the associated data in the data source object.
	 */
	public static final String onbeforeupdate = "onbeforeupdate";

	/**
	 * Fires when the object loses the input focus.
	 */
	public static final String onblur = "onblur";

	/**
	 * Fires when the behavior property of the marquee object is set to "alternate" 
	 * and the contents of the marquee reach one side of the window.
	 */
	public static final String onbounce = "onbounce";

	/**
	 * Fires when data changes in the data provider.
	 */
	public static final String oncellchange = "oncellchange";

	/**
	 * Fires when the contents of the object or selection have changed.
	 */
	public static final String onchange = "onchange";

	/**
	 * Fires when the user clicks the left mouse button on the object.
	 */
	public static final String onclick = "onclick";

	/**
	 * Fires when the user clicks the right mouse button in the client area, opening the context menu.
	 */
	public static final String oncontextmenu = "oncontextmenu";

	/**
	 * Fires when the user is about to make a control selection of the object.
	 */
	public static final String oncontrolselect = "oncontrolselect";

	/**
	 * Fires on the source element when the user copies the object or selection, 
	 * adding it to the system clipboard.
	 */
	public static final String oncopy = "oncopy";

	/**
	 * Fires on the source element when the object or selection is removed from the document and 
	 * added to the system clipboard.
	 */
	public static final String oncut = "oncut";

	/**
	 * Fires periodically as data arrives from data source objects that asynchronously transmit 
	 * their data.
	 */
	public static final String ondataavailable = "ondataavailable";

	/**
	 * Fires when the data set exposed by a data source object changes.
	 */
	public static final String ondatasetchanged = "ondatasetchanged";

	/**
	 * Fires to indicate that all data is available from the data source object.
	 */
	public static final String ondatasetcomplete = "ondatasetcomplete";

	/**
	 * Fires when the user double-clicks the object.
	 */
	public static final String ondblclick = "ondblclick";

	/**
	 * Fires when the activeElement is changed from the current object to another object in the parent document.
	 */
	public static final String ondeactivate = "ondeactivate";

	/**
	 * Fires on the source object continuously during a drag operation.
	 */
	public static final String ondrag = "ondrag";

	/**
	 * Fires on the source object when the user releases the mouse at the close of a drag operation.
	 */
	public static final String ondragend = "ondragend";

	/**
	 * Fires on the target element when the user drags the object to a valid drop target.
	 */
	public static final String ondragenter = "ondragenter";

	/**
	 * Fires on the target object when the user moves the mouse out of a valid drop 
	 * target during a drag operation.
	 */
	public static final String ondragleave = "ondragleave";

	/**
	 * Fires on the target element continuously while the user drags the object over a 
	 * valid drop target.
	 */
	public static final String ondragover = "ondragover";

	/**
	 * Fires on the source object when the user starts to drag a text selection or selected object.
	 */
	public static final String ondragstart = "ondragstart";

	/**
	 * Fires on the target object when the mouse button is released during a drag-and-drop operation.
	 */
	public static final String ondrop = "ondrop";

	/**
	 * Fires when an error occurs during object loading.
	 */
	public static final String onerror = "onerror";

	/**
	 * Fires on a databound object when an error occurs while updating the associated data in the data source object.
	 */
	public static final String onerrorupdate = "onerrorupdate";

	/**
	 * Fires when a visual filter changes state or completes a transition.
	 */
	public static final String onfilterchange = "onfilterchange";

	/**
	 * Fires when marquee looping is complete.
	 */
	public static final String onfinish = "onfinish";

	/**
	 * Fires when the object receives focus.
	 */
	public static final String onfocus = "onfocus";

	/**
	 * Fires for an element just prior to setting focus on that element. 
	 */
	public static final String onfocusin = "onfocusin";

	/**
	 * Fires for the current element with focus immediately after moving focus to another element.
	 */
	public static final String onfocusout = "onfocusout";

	/**
	 * Fires when the user presses the F1 key while the browser is the active window.
	 */
	public static final String onhelp = "onhelp";
	
	/**
	 * Fires when the user presses a key.
	 */
	public static final String onkeydown = "onkeydown";

	/**
	 * Fires when the user presses an alphanumeric key.
	 */
	public static final String onkeypress = "onkeypress";

	/**
	 * Fires when the user releases a key.
	 */
	public static final String onkeyup = "onkeyup";

	/**
	 * Fires when the print or print preview layout process finishes filling the current 
	 * LayoutRect object with content from the source document.
	 */
	public static final String onlayoutcomplete = "onlayoutcomplete";

	/**
	 * Fires immediately after the browser loads the object.
	 */
	public static final String onload = "onload";

	/**
	 * Fires when the object loses the mouse capture.
	 */
	public static final String onlosecapture = "onlosecapture";

	/**
	 * Fires when the user clicks the object with either mouse button.
	 */
	public static final String onmousedown = "onmousedown";

	/**
	 * Fires when the user moves the mouse pointer into the object.
	 */
	public static final String onmouseenter = "onmouseenter";

	/**
	 * Fires when the user moves the mouse pointer outside the boundaries of the object.
	 */
	public static final String onmouseleave = "onmouseleave";

	/**
	 * Fires when the user moves the mouse over the object.
	 */
	public static final String onmousemove = "onmousemove";
	
	/**
	 * Fires when the user moves the mouse pointer outside the boundaries of the object.
	 */
	public static final String onmouseout = "onmouseout";

	/**
	 * Fires when the user moves the mouse pointer into the object.
	 */
	public static final String onmouseover = "onmouseover";

	/**
	 * Fires when the user releases a mouse button while the mouse is over the object.
	 */
	public static final String onmouseup = "onmouseup";

	/**
	 * Fires when the wheel button is rotated.
	 */
	public static final String onmousewheel = "onmousewheel";

	/**
	 * Fires when the object moves.
	 */
	public static final String onmove = "onmove";

	/**
	 * Fires when the object stops moving.
	 */
	public static final String onmoveend = "onmoveend";

	/**
	 * Fires when the object starts to move.
	 */
	public static final String onmovestart = "onmovestart";

	/**
	 * Fires on the target object when the user pastes data, transferring the data from 
	 * the system clipboard to the document.
	 */
	public static final String onpaste = "onpaste";

	/**
	 * Fires when a property changes on the object.
	 */
	public static final String onpropertychange = "onpropertychange";

	/**
	 * Fires when the state of the object has changed.
	 */
	public static final String onreadystatechange = "onreadystatechange";

	/**
	 * Fires when the user resets a form.
	 */
	public static final String onreset = "onreset";

	/**
	 * Fires when the size of the object is about to change.
	 */
	public static final String onresize = "onresize";

	/**
	 * Fires when the user finishes changing the dimensions of the object in a control selection.
	 */
	public static final String onresizeend = "onresizeend";

	/**
	 * Fires when the user begins to change the dimensions of the object in a control selection.
	 */
	public static final String onresizestart = "onresizestart";

	/**
	 * Fires to indicate that the current row has changed in the data source and 
	 * new data values are available on the object. 
	 */
	public static final String onrowenter = "onrowenter";

	/**
	 * Fires just before the data source control changes the current row in the object.
	 */
	public static final String onrowexit = "onrowexit";

	/**
	 * Fires when rows are about to be deleted from the recordset.
	 */
	public static final String onrowsdelete = "onrowsdelete";

	/**
	 * Fires just after new rows are inserted in the current recordset.
	 */
	public static final String onrowsinserted = "onrowsinserted";

	/**
	 * Fires when the user repositions the scroll box in the scroll bar on the object.
	 */
	public static final String onscroll = "onscroll";

	/**
	 * Fires when the current selection changes.
	 */
	public static final String onselect = "onselect";

	/**
	 * Fires when the selection state of a document changes.
	 */
	public static final String onselectionchange = "onselectionchange";

	/**
	 * Fires when the object is being selected.
	 */
	public static final String onselectstart = "onselectstart";

	/**
	 * Fires at the beginning of every loop of the marquee object.
	 */
	public static final String onstart = "onstart";

	/**
	 * Fires when the user clicks the Stop button or leaves the Web page.
	 */
	public static final String onstop = "onstop";

	/**
	 * Fires when a FORM is about to be submitted.
	 */
	public static final String onsubmit = "onsubmit";

	/**
	 * Fires immediately before the object is unloaded.
	 */
	public static final String onunload = "onunload";

}
